    public FrmMercadoProdutos(FrmMercadoPrincipal frame) {
        this();
        this.frame = frame;
        this.produtos = frame.getProdutos();
        for(Produto produto : produtos) {
            modelProdutos.addElement(produto.toString());            
        }
        tfCodigoProduto.setText(String.valueOf(produtos.size() + 1));
        lstProdutos.setModel(modelProdutos);
    }
    
