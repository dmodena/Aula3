    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        produto = new Produto(Integer.parseInt(tfCodigoProduto.getText()), tfDescricaoProduto.getText(), Double.parseDouble(tfPrecoProduto.getText()));
        produtos.add(produto);
        modelProdutos.addElement(produto.toString());
        lstProdutos.setModel(modelProdutos);
        limpaCampos();
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnFecharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFecharActionPerformed
        frame.setProdutos(produtos);
        this.dispose();
    }//GEN-LAST:event_btnFecharActionPerformed

    public void limpaCampos() {
        tfCodigoProduto.setText(String.valueOf(produtos.size() + 1));
        tfDescricaoProduto.setText("");
        tfPrecoProduto.setText("");
        tfDescricaoProduto.requestFocus();
    }
    
