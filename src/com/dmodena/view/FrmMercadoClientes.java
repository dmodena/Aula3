    private void btnCadastrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarClienteActionPerformed
        cliente = new Cliente(Integer.parseInt(tfCodigoCliente.getText()), tfNomeCliente.getText(), tfTelefoneCliente.getText());
        clientes.add(cliente);
        modelClientes.addElement(cliente.toString());        
        lstClientes.setModel(modelClientes);
        limpaCampos();
    }//GEN-LAST:event_btnCadastrarClienteActionPerformed

    private void btnFecharActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFecharActionPerformed
        frame.setClientes(clientes);
        this.dispose();
    }//GEN-LAST:event_btnFecharActionPerformed

    public void limpaCampos() {
        tfCodigoCliente.setText(String.valueOf(clientes.size() + 1));
        tfNomeCliente.setText("");
        tfTelefoneCliente.setText("");
        tfNomeCliente.requestFocus();
    }
    
